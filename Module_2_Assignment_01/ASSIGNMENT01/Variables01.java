// Assign decimal, octal, hexadecimal values to variables and print

class Variables01
{      static int a = 100;
	   static int b = 050;
	   static int c = 0xABC;
	   static int d = 'A';
	public static void main (String args[])
	{  
	   System.out.println(a);
	   System.out.println(b);
	   System.out.println(c);
	   System.out.println(d);
	}
}