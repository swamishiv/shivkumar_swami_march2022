// Table of 1 to 30 in vertical by using single loop

class TableDoubleForLoop02
{
	public static void main (String args[])
	{
	      for (int i=1; i<=20; i++)
		    {    int ans =1;
		         ans*=i;
	            System.out.println();  
		        System.out.print(ans);
			 		
	        }
           
	}
}